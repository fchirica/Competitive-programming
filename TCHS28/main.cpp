class ProdCalc {
public:
	long long highest(int, int);
};

int n, sol = -1, upp;

void go(int pos, int dig, int addOp, int val)
{
	if (dig > 9 || val >= upp)
		return ;
	if (addOp == 0)
		if (val > sol)
			sol = val;
	go(pos + 1, dig, addOp - 1, val * dig);
	go(pos, dig + 1, addOp, val);
}

long long ProdCalc::highest(int d, int op) {
	n = d;
	upp = 1;
	int i;
	for (i = 1; i <= n; i ++)
		upp = upp * 10;
	go(1, 2, op, 1);
	return sol;
}


